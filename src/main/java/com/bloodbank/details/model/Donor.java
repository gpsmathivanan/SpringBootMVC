package com.bloodbank.details.model;

public class Donor {
	private String name;
	private String gender;
	private String dateofBirth;
	private String phone;
	private String homeAddress;
	private String bloodType;
	private String anydiseases;
	private String positiveBlood;
	private String allergies;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDateofBirth() {
		return dateofBirth;
	}
	public void setDateofBirth(String dateofBirth) {
		this.dateofBirth = dateofBirth;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getHomeAddress() {
		return homeAddress;
	}
	public void setHomeAddress(String homeAddress) {
		this.homeAddress = homeAddress;
	}
	public String getBloodType() {
		return bloodType;
	}
	public void setBloodType(String bloodType) {
		this.bloodType = bloodType;
	}
	public String getAnydiseases() {
		return anydiseases;
	}
	public void setAnydiseases(String anydiseases) {
		this.anydiseases = anydiseases;
	}
	public String getPositiveBlood() {
		return positiveBlood;
	}
	public void setPositiveBlood(String positiveBlood) {
		this.positiveBlood = positiveBlood;
	}
	public String getAllergies() {
		return allergies;
	}
	public void setAllergies(String allergies) {
		this.allergies = allergies;
	}
	
	

}