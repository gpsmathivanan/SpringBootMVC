package com.bloodbank.details.model;

public class Seeker {
	private String name;
	private String gender;
	private String tillRequiredDate;
	private String phone;
	private String homeAddress;
	private String bloodType;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getTillRequiredDate() {
		return tillRequiredDate;
	}
	public void setTillRequiredDate(String tillRequiredDate) {
		this.tillRequiredDate = tillRequiredDate;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getHomeAddress() {
		return homeAddress;
	}
	public void setHomeAddress(String homeAddress) {
		this.homeAddress = homeAddress;
	}
	public String getBloodType() {
		return bloodType;
	}
	public void setBloodType(String bloodType) {
		this.bloodType = bloodType;
	}
	
	
	
}
